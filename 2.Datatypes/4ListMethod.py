print("List Method")

list = [20,31,12,53,34,65,76,87,18,29]
print(list)

list.sort()             #arranging the list in ascending order
print(list)

list.reverse()          #arranging the list in descending order
print(list)

list.append(54)         #add character in the last 
print(list)

list.insert(0,22)       #(index_value,value) insertion
print(list)

list.pop(2)             #(index_value)removing the element from index 2
print(list)

list.remove(65)         #(value_of_Int) removes element from the list
print(list)